# When to use?
#Used to combine multiple statements!

# "and", returns True if both conditions are True

a = 8
print(a > 2 and a < 9)

# "or", returns True if at least one condition is True
a = 10
print(a > 5 or a < 7)

# "not" , Reverse the condition, returns false if the condition is true!

a = 9
print(not(a > 3 and a < 10))