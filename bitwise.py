# when to use?
# used to compare binary numbers

# Bitwise "and" (&)
a = 12  
b = 5   
result = a & b  
print(result)  

#Bitwise "or" (|)
a = 12  
b = 5   
result = a | b  
print(result) 

# Bitwise "xor" (^)
a = 12  
b = 5   
result = a ^ b  
print(result)  

# Bitwise "not" (~)
a = 5   
result = ~a  
print(result)  

# Bitwise "left shift" (<<)
a = 3  
result = a << 2  
print(result) 

# Bitwise Right Shift (>>)
a = 16  
result = a >> 3  
print(result)  





