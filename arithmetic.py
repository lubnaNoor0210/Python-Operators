# When to Use?
# Whenever you want to add, subtract, multiply, divide, find modulus, exponent, or floor division between numbers.

a = 10
b = 3

print("Addition:", a + b)
print("Subraction:", a - b)
print("Division:" , a / b)
print("Multiplication:" , a * b)
print("Modulus:" , a % b)
print("Exponent:" , a ** b)
print("Floor Division:" , a // b)